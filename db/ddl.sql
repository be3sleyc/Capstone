CREATE TABLE Users (
    id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY, 
    givenname VARCHAR(30) NOT NULL, 
    surname VARCHAR(30) NOT NULL, 
    email VARCHAR(50) NOT NULL, 
    password BINARY(128) NOT NULL, 
    spice BINARY(128) NOT NULL,  
    lastaccess DATETIME
    );
