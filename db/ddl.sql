CREATE TABLE Transactions (
    id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    payerid INT UNSIGNED NOT NULL FORIEGN KEY REFERENCES Users(id),
    paiddate DATETIME NOT NULL DEFAULT GETDATE(), 
    payee VARCHAR(100) NOT NULL,
